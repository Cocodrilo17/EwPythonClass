# AND
print(False & False)
print(False & True)
print(True & False)
print(True & True)

print(4 & 6)

# OR
print(False | False)
print(False | True)
print(True | False)
print(True | True)

# NOT
print(~True)
print(~False)

# XOR
print(False ^ False)
print(False ^ True)
print(True ^ False)
print(True ^ True)

# SHIFT LEFT
print(10 << 2)

# SHIFT RIGHT
print(10 >> 2)
